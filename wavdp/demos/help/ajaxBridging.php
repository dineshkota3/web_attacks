<h2 id="8">8. AJAX Bridging</h2>
<br>
<p>

<u>Attack Scenario</u>: Consider a website <i>site1</i> (e.g. mashup) which depends on some other sites <i>site2</i> to update the information (e.g. news etc.). Assume <i>site2</i> sends the data in JSON format and site1 trusts site2 so displays the information received without any sanitization. So if the <i>site2</i> is somehow compromised, site1 can also be compromised.

